void main() {

  //var
  var name = "Flutter";
  print("Name: $name");

  name = "Festival";
  print("Updated Name: $name");

  //final
  final age = 21;
  print("Age: $age");

  final List<int> numbers = [1, 2, 3];
  numbers.add(4);
  print("Numbers: $numbers");
  //const
  const pi = 3.14;
  print("Pi: $pi");

  const List<int> fixedNumbers = [1, 2, 3];
  fixedNumbers.add(4);
  print("Fixed Numbers: $fixedNumbers");
}
